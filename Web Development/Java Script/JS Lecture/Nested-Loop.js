// Nested Loop
// Nested Loop of Multiplication Table
console.log("======================Multiplication Table======================");
for(let i=1;i<=10;i++){
    for(let j=1;j<=10;j++){
        console.log(`${i} x ${j} = ${i*j}`);
    }
}




// Triangle Star Pattern
console.log("======================Triangle Star Pattern======================");

for(let i=1;i<=5;i++){
    let str = '';
    for(let j=1;j<=i;j++){
        str += '*';
    }
    console.log(str);
}







